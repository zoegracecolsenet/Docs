"""---

## Question 7 (3 points)

**This is the main challenge: Extract all dates from the medical notes and sort them chronologically.**

**Rules:**
- Assume all dates in `xx/xx/xx` format are `mm/dd/yy`
- Assume all 2-digit years are from the 1900s (e.g., `1/5/89` is January 5th, 1989)
- If the day is missing (e.g., `9/2009`), assume it is the 1st day of the month
- If the month is missing (e.g., `2010`), assume it is January 1st

*This function should return a pandas Series of length 500, where the values are the original indices sorted by date in ascending chronological order.*

**Example:**
```python
# If original series was:
#    0    1999
#    1    2010
#    2    1978
# Your function should return:
#    0    2    (1978 is earliest)
#    1    0    (1999 is second)
#    2    1    (2010 is latest)
```
"""

def question_seven():
    """
    Extract dates from all medical notes and return indices sorted chronologically.

    Returns:
        pd.Series: Series of length 500 with original indices sorted by date
    """

    month_re = r'(?:Jan|January|Feb|February|Mar|March|Apr|April|May|Jun|June|Jul|July|Aug|August|Sep|Sept|September|Oct|October|Nov|November|Dec|December)'

    patterns = [
        ('mdy', re.compile(r'\b\d{1,2}/\d{1,2}/\d{2,4}\b')),
        ('my_num', re.compile(r'\b\d{1,2}/\d{4}\b')),
        ('month_day_year', re.compile(rf'\b{month_re}\.?(?:[-\s]+)\d{{1,2}}(?:,)?(?:[-\s]+)\d{{4}}\b', re.I)),
        ('day_month_year', re.compile(rf'\b\d{{1,2}}(?:[-\s]+){month_re}\.?(?:,)?(?:[-\s]+)\d{{4}}\b', re.I)),
        ('month_year', re.compile(rf'\b{month_re}\.?(?:[-\s]+)\d{{4}}\b', re.I)),
        ('year', re.compile(r'\b(?:19|20)\d{2}\b'))
    ]

    month_map = {
        'jan': 1, 'feb': 2, 'mar': 3, 'apr': 4, 'may': 5, 'jun': 6,
        'jul': 7, 'aug': 8, 'sep': 9, 'oct': 10, 'nov': 11, 'dec': 12
    }

    def parse_date(text, kind):
        if kind == 'mdy':
            m, d, y = text.split('/')
            m, d, y = int(m), int(d), int(y)
            if y < 100:
                y += 1900
            return datetime(y, m, d)

        elif kind == 'my_num':
            m, y = text.split('/')
            return datetime(int(y), int(m), 1)

        elif kind == 'month_day_year':
            cleaned = re.sub(r'[,.]', '', text).replace('-', ' ')
            parts = cleaned.split()
            return datetime(int(parts[2]), month_map[parts[0][:3].lower()], int(parts[1]))

        elif kind == 'day_month_year':
            cleaned = re.sub(r'[,.]', '', text).replace('-', ' ')
            parts = cleaned.split()
            return datetime(int(parts[2]), month_map[parts[1][:3].lower()], int(parts[0]))

        elif kind == 'month_year':
            cleaned = re.sub(r'[,.]', '', text).replace('-', ' ')
            parts = cleaned.split()
            return datetime(int(parts[1]), month_map[parts[0][:3].lower()], 1)

        else:
            return datetime(int(text), 1, 1)

    parsed = []

    for idx, note in df.items():
        matches = []

        for order, (kind, pattern) in enumerate(patterns):
            for match in pattern.finditer(note):
                matches.append((match.start(), order, kind, match.group()))

        if matches:
            matches.sort(key=lambda x: (x[0], x[1]))
            best_match = matches[0]
            date_obj = parse_date(best_match[3], best_match[2])
        else:
            date_obj = datetime(1900, 1, 1)

        parsed.append((idx, date_obj))

    result = pd.DataFrame(parsed, columns=['index', 'date'])
    result = result.sort_values('date', kind='mergesort')

    return result['index'].reset_index(drop=True)# Return the sorted indices

