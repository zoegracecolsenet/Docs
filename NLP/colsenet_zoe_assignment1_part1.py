def question_one():
    """
    Extract all dates in MM/DD/YY or MM/DD/YYYY format.

    Returns:
        list: List of matched date strings
    """
    # YOUR CODE HERE
    pattern = r'\b\d{1,2}/\d{1,2}/(?:\d{2}|\d{4})\b'

    results = []
    for note in df:
        matches = re.findall(pattern, note)
        results.extend(matches)

